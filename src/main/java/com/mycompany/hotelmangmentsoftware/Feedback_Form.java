    private void populateFeedbackTable() {
        DefaultTableModel model = (DefaultTableModel) feedbackTable.getModel();
        for (String[] feedback : feedbackList) {
            model.addRow(new Object[]{feedback[0], feedback[1], feedback[2]});
        }
    }

