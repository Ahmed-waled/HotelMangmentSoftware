    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        String review = Comment.getText();
        float rating = Float.parseFloat(Rating_Value.getText());
        int guestID = guest.getGuestId();
        int hotelID = hotel.getId();
        // Call the makeFeedback method from Feedback class
        Feedback feedback = new Feedback(review, rating, guestID, hotelID);
        boolean success = feedback.makeFeedback();

        // Handle the result
        if (success) {
            JOptionPane.showMessageDialog(this, "Feedback submitted successfully.");
            // Add the new feedback to the table
            DefaultTableModel model = (DefaultTableModel) feedbackTable.getModel();
            model.addRow(new Object[]{guest.getUsername(), review, rating});
        } else {
            JOptionPane.showMessageDialog(this, "Failed to submit feedback.");
        }
    }//GEN-LAST:event_submitActionPerformed

